#include <stdio.h>

int main() {

    // Assigning values to variables
    int a = 82;
    float b = 42.0;
    char c = "M";

    // Printing the variables
    printf("the value of the integer 'a': %d\n", a);
    printf("the value of float 'b': %.1f\n", b);
    printf("the value of character 'c': %d", c);

    return 0;
}